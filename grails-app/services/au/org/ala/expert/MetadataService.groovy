    def getImcraPolyAsWkt(pid) {
        if (imcraWktCache[pid]) {
            return imcraWktCache[pid]
        }

        def wkt = webService.get(grailsApplication.config.spatial.layers.service.url + "/shape/wkt/${pid}")

